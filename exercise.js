let currentExerciseArray = [];

const getRandomExercises = (exerciseArray) => {
  if (!Array.isArray(exerciseArray) || exerciseArray.length === 0) {
    console.error('Exercise array is not properly defined');
    return [];
  }

  for (let i = 0; i < 54; i++) {
    const randomIndex = Math.floor(Math.random() * exerciseArray.length);
    const exercise = exerciseArray[randomIndex];
    if (!exercise) {
      console.warn(`Exercise at index ${randomIndex} is undefined`);
      continue;
    }

    let exerciseData = {
      name: exercise.exercise,
      description: exercise.description
    }
    currentExerciseArray.push(exerciseData)
  }
  console.log(currentExerciseArray)
  return currentExerciseArray
}


